console.log("--While--");
var x = 0;
while (x < 10) {
  // a loops that increments x, and logs it
  console.log(x);
  x++;
}

console.log("--DO While--");
var y = 0;
do {
  // same here
  console.log(y);
  y++;
} while (y < 10);

console.log("--For--");
var z;
for (z = 0; z < 10; z++) {
  console.log(z);
}
